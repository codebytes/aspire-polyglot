#!/usr/bin/env node
// Writes src/assets/otel-config.js from environment variables at npm start time.
// Aspire AppHost injects OTEL_EXPORTER_OTLP_ENDPOINT (gRPC) and we map it to
// the dashboard's HTTP/protobuf port (browser cannot speak gRPC).
//
// Lookup order for the browser-reachable OTLP endpoint:
//   1. OTEL_EXPORTER_OTLP_ENDPOINT_HTTP  (explicit override)
//   2. OTEL_EXPORTER_OTLP_ENDPOINT       (rewritten gRPC port -> HTTP port)
//   3. http://localhost:4318             (sensible default)

const fs = require('node:fs');
const path = require('node:path');

const grpcEndpoint = process.env.OTEL_EXPORTER_OTLP_ENDPOINT || '';
const httpEndpointEnv = process.env.OTEL_EXPORTER_OTLP_ENDPOINT_HTTP || '';

function rewriteToHttp(grpcUrl) {
  if (!grpcUrl) return '';
  try {
    const u = new URL(grpcUrl);
    return u.toString().replace(/\/+$/, '');
  } catch {
    return '';
  }
}

const otlpEndpoint =
  httpEndpointEnv ||
  rewriteToHttp(grpcEndpoint) ||
  'http://localhost:4318';

const serviceName = process.env.OTEL_SERVICE_NAME || 'frontend';
const serviceVersion = process.env.OTEL_SERVICE_VERSION || '1.0.0';

const target = path.resolve(__dirname, '..', 'src', 'assets', 'otel-config.js');

const body = `// Auto-generated by scripts/write-otel-config.cjs at start time.
// Do not commit changes - values come from Aspire-injected env vars.
window.__OTEL_CONFIG__ = ${JSON.stringify(
  { otlpEndpoint, serviceName, serviceVersion },
  null,
  2
)};
`;

fs.writeFileSync(target, body, 'utf8');
console.log(
  `[otel-config] wrote ${target}\n             endpoint=${otlpEndpoint}\n             service=${serviceName}@${serviceVersion}`
);
